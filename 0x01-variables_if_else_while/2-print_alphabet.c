/**
 * main - Entry point
 *
 * Return: Always 0 (Success)
 *
 *for loop from a-z.
 * note that doing ++ to a letter character moves it to the next alphabet char
 *   Case sensitive
 */
int main(void)
{

char letter;

for (letter = 'a'; letter <= 'z'; letter++)
{
putchar(letter);
}
putchar('\n');
return (0);
}
